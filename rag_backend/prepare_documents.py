chapters["03_线性模型"] = """# 第三章 线性模型

## 3.1 基本形式

线性模型（Linear Model）试图学得一个通过属性的线性组合来进行预测的函数：
f(x) = w1·x1 + w2·x2 + ... + wd·xd + b = w^T·x + b

其中 w（权重向量）和 b（偏置）是需要学习的参数。线性模型形式简单、易于解释，很多非线性模型都是在线性模型基础上发展而来的。

## 3.2 线性回归

线性回归（Linear Regression）试图学得一个线性模型以尽可能准确地预测实值输出标记。

最小二乘法（Least Squares Method）：通过最小化均方误差来求解线性回归的参数 w 和 b。对损失函数分别求 w 和 b 的偏导数并令其为零，可得到闭式解。

多元线性回归可以写成矩阵形式：ŷ = Xw，其中 X 是设计矩阵。当 X^T·X 可逆时，最优解为 w = (X^T·X)^(-1)·X^T·y。

梯度下降（Gradient Descent）：一种迭代优化方法。沿损失函数梯度的反方向更新参数：w = w - η·∇L(w)，其中 η 是学习率。
- 批量梯度下降（BGD）：每次用全部数据计算梯度
- 随机梯度下降（SGD）：每次用一个样本计算梯度，速度快但波动大
- 小批量梯度下降（Mini-batch GD）：折中方案，每次用一小批数据

## 3.3 对数几率回归

对数几率回归（Logistic Regression），虽然名字中有"回归"，但实际上是一种分类模型。

它使用 Sigmoid 函数（S 型函数）将线性输出映射到 (0,1) 区间：
y = 1 / (1 + e^(-(w^T·x + b)))

输出值可以解释为样本属于正类的概率。当 y > 0.5 时预测为正类，否则为负类。决策边界是一个超平面。

对数几率回归使用对数似然损失函数，通过梯度下降或牛顿法来求解最优参数。

## 3.4 线性判别分析

线性判别分析（LDA, Linear Discriminant Analysis）是一种经典的线性分类方法。其核心思想是：将样本投影到一条直线上，使得同类样本的投影点尽可能接近，异类样本的投影点尽可能远离。

LDA 也可用于降维：将数据投影到最能区分类别的方向上，降低维度的同时保留类别判别信息。

## 3.5 多分类学习

对于多分类问题（类别数 > 2），通常将其拆分为多个二分类问题：

一对一（One vs. One, OvO）：将 N 个类别两两配对，产生 N(N-1)/2 个二分类任务。每个分类器只使用对应两个类别的样本训练。最终通过投票法决定类别。

一对其余（One vs. Rest, OvR）：每次将一个类别的样本作为正例，其他所有类别作为负例，训练 N 个分类器。预测时选择置信度最大的类别。

## 3.6 正则化

正则化（Regularization）是在损失函数中加入惩罚项，以限制模型复杂度，防止过拟合。

L1 正则化（LASSO）：惩罚项为参数绝对值之和。能产生稀疏解，自动进行特征选择。
L2 正则化（Ridge）：惩罚项为参数平方和。使参数趋向较小的值但不会为零。
"""

