chapters["04_决策树"] = """# 第四章 决策树

## 4.1 基本流程

决策树（Decision Tree）是一类常见的机器学习方法，基于树形结构来进行决策。一般地，一棵决策树包含一个根节点、若干内部节点和若干叶节点。叶节点对应于决策结果（类别标记），其他每个节点对应于一个属性测试。

决策树的学习过程通常采用自顶向下的递归分裂策略：从根节点开始，选择最优属性进行划分，然后对每个子节点递归执行同样的过程，直到满足停止条件。

## 4.2 划分选择

决策树学习的关键是如何选择最优划分属性。不同的划分准则产生了不同的决策树算法。

### 信息熵与信息增益

信息熵（Information Entropy）：度量样本集合纯度的指标。对于包含 K 个类别的数据集，信息熵为：
Ent(D) = -Σ(k=1 to K) p_k · log₂(p_k)
其中 p_k 是第 k 类样本所占比例。信息熵越小，样本集合纯度越高。

信息增益（Information Gain）：使用属性 a 对数据集 D 进行划分所获得的信息增益：
Gain(D, a) = Ent(D) - Σ(v=1 to V) (|D^v|/|D|) · Ent(D^v)
信息增益越大，说明使用该属性划分后纯度提升越多。

### ID3 算法

ID3 使用信息增益作为划分准则，每次选择信息增益最大的属性进行划分。缺点是倾向于选择取值较多的属性（例如"编号"这样的属性信息增益最大但毫无意义）。

### 信息增益比与 C4.5

信息增益比（Gain Ratio）：对信息增益进行修正，除以属性的固有值（Intrinsic Value）：
Gain_ratio(D, a) = Gain(D, a) / IV(a)
其中 IV(a) = -Σ(v=1 to V) (|D^v|/|D|) · log₂(|D^v|/|D|)

C4.5 算法使用信息增益比作为划分准则，克服了 ID3 偏好多值属性的缺点。C4.5 还支持连续值处理（二分法）和缺失值处理。

### 基尼指数与 CART

基尼指数（Gini Index）：度量样本集合不纯度的指标：
Gini(D) = 1 - Σ(k=1 to K) p_k²
基尼指数越小，纯度越高。

CART（Classification and Regression Tree）使用基尼指数作为划分准则，每次选择基尼指数最小的属性进行划分。CART 生成的是二叉树（每个节点最多有两个子节点）。

## 4.3 剪枝处理

剪枝（Pruning）是决策树对付过拟合的主要手段，通过去除部分节点来简化决策树。

预剪枝（Pre-pruning）：在决策树生成过程中，对每个节点在划分前先进行估计。若划分不能带来泛化性能的提升（如在验证集上准确率没有提高），则停止划分并将当前节点标记为叶节点。预剪枝降低了过拟合风险，但可能带来欠拟合风险（有些分支的当前划分虽暂时不能提升性能，但后续划分可能大幅提升）。

后剪枝（Post-pruning）：先生成一棵完整的决策树，然后自底向上地对非叶节点逐一考察。若将该子树替换为叶节点能提升泛化性能，则进行剪枝。后剪枝一般比预剪枝保留了更多分支，泛化性能往往更好，但训练开销更大。
"""

