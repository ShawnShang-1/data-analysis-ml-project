chapters["16_强化学习"] = """# 第十六章 强化学习

## 16.1 强化学习的基本概念

强化学习（Reinforcement Learning, RL）是一种通过与环境交互来学习最优行为策略的机器学习范式。与监督学习不同，强化学习没有"正确答案"，而是通过奖励信号来引导学习。

基本要素：
- 智能体（Agent）：学习和决策的主体
- 环境（Environment）：智能体所处的外部世界
- 状态（State）：环境在某一时刻的描述
- 动作（Action）：智能体在某个状态下可以执行的操作
- 奖励（Reward）：环境对智能体动作的即时反馈信号
- 策略（Policy）：从状态到动作的映射，即智能体的行为规则

强化学习的目标是找到一个策略，使得智能体在长期交互中获得的累积奖励最大化。

## 16.2 马尔可夫决策过程

马尔可夫决策过程（MDP, Markov Decision Process）是强化学习的数学框架，由五元组 (S, A, P, R, γ) 定义：
- S：状态空间
- A：动作空间
- P：状态转移概率 P(s'|s,a)
- R：奖励函数 R(s,a)
- γ：折扣因子（0<γ<1），控制未来奖励的权重

马尔可夫性质：未来状态只取决于当前状态和动作，与历史无关。

## 16.3 值函数

值函数（Value Function）：从某个状态（或状态-动作对）出发，遵循某个策略的期望累积奖励。

状态值函数 V(s)：从状态 s 出发，遵循策略 π 的期望回报。
Q 值函数 Q(s,a)：在状态 s 执行动作 a 后，遵循策略 π 的期望回报。

最优值函数：所有策略中值函数的最大值。最优策略就是使值函数最大的策略。

## 16.4 经典算法

Q-learning：一种基于时序差分（TD）的无模型（model-free）强化学习算法。通过不断与环境交互来学习最优 Q 值函数：
Q(s,a) ← Q(s,a) + α[r + γ·max_a' Q(s',a') - Q(s,a)]
Q-learning 是一种异策略（off-policy）算法，学习的是最优策略的 Q 值。

Sarsa：与 Q-learning 类似，但是一种同策略（on-policy）算法。更新时使用实际执行的下一个动作的 Q 值而非最大 Q 值：
Q(s,a) ← Q(s,a) + α[r + γ·Q(s',a') - Q(s,a)]
Sarsa 更加"保守"和"安全"，适合风险敏感的场景。

策略梯度（Policy Gradient）：直接参数化策略函数 π(a|s;θ)，通过梯度上升来最大化期望回报。不需要值函数，但方差较大。

Actor-Critic：结合策略梯度（Actor，负责选动作）和值函数（Critic，负责评价动作好坏）的混合架构。Actor 根据 Critic 的评价来更新策略，Critic 根据实际回报来更新值函数估计。

## 16.5 深度强化学习

DQN（Deep Q-Network）：用深度神经网络来近似 Q 值函数。主要创新：
- 经验回放（Experience Replay）：存储历史经验并随机采样训练，打破数据相关性
- 目标网络（Target Network）：使用单独的网络计算目标值，稳定训练

DQN 的变体：Double DQN（解决 Q 值过高估计）、Dueling DQN（分离状态值和优势函数）、Prioritized Replay（优先回放重要经验）等。

探索与利用（Exploration vs. Exploitation）：强化学习中的核心权衡。探索是尝试未知动作以获取新信息，利用是选择当前已知的最优动作。ε-greedy 策略是最简单的平衡方式：以 ε 概率随机探索，以 1-ε 概率选择最优动作。
"""


